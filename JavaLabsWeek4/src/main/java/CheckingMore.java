public class CheckingMore {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            System.out.println("Square of " + i + " is " + (i * 2));
        }
        // d
        int i = 0;
        while (i < 3) {
            System.out.println("Square of " + i + " is " + (i * 2));
            i++; // increment at end
        }

        // e

        System.out.println("Square of " + 0 + " is " + (0 * 2));
        System.out.println("Square of " + 1 + " is " + (1 * 2));
        System.out.println("Square of " + 2 + " is " + (2 * 2));


    }
}
